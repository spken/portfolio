import { useInView } from 'framer-motion'
import { useRef } from 'react'

export const useScrollAnimation = (
  options: {
    amount?: number
    once?: boolean
  } = {}
) => {
  const ref = useRef(null)
  const isInView = useInView(ref, {
    amount: options.amount || 0.1,
    once: options.once !== false
  })

  return { ref, isInView }
}

